    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int selectedIndex = lstCursos.getSelectedIndex();
        if (selectedIndex != -1) {
         Curso cursoSeleccionado = cursos.get(selectedIndex);
         llamarPantallaEdicion(cursoSeleccionado);
         } 
        
    
    }//GEN-LAST:event_btnEditarActionPerformed
    
    public void updateCurso(Curso curso) {
    int selectedIndex = lstCursos.getSelectedIndex();
    cursos.set(selectedIndex, curso);
    modelo.set(selectedIndex, curso.getTexto());
    }
    
    private void llamarPantallaEdicion(){
        PantallaEditarCurso curso = new PantallaEditarCurso(this);
        curso.setLocationRelativeTo(null);
        curso.setVisible(true);
        this.setVisible(false);
    }
    private void llamarPantallaEdicion(Curso curso){
        PantallaEditarCurso pantallacurso = new PantallaEditarCurso(this, curso);
        pantallacurso.setLocationRelativeTo(null);
        pantallacurso.setVisible(true);
    }


    public void agregarCurso(Curso curso) {
    cursos.add(curso);
    modelo.addElement(curso.getTexto());
    lstCursos.setModel(modelo);
    }
