    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int selectedIndex = lstCursos.getSelectedIndex();
        if (selectedIndex != -1) {
        cursos.remove(selectedIndex);
        modelo.removeElementAt(selectedIndex);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
          // TODO add your handling code here:
           String BuscarGestCurso = txtBuscarGestCur.getText().toLowerCase(); 
          modelo.clear();
          for (Curso curso : cursos) {
                   String cursoNombre = curso.getCodigo().toLowerCase();
                   if (cursoNombre.contains(BuscarGestCurso)) {
                   modelo.addElement(curso.getTexto());
          }
    }
    lstCursos.setModel(modelo);
          
    }//GEN-LAST:event_btnBuscarActionPerformed

